const seedKDMP = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI);
    console.log("✅ DB Connected");

    const lines = rawData.split("\n").map(l => l.trim()).filter(l => l);
    const tasks = [];
    
    // Setiap entitas 8 baris
    // 1. Nama
    // 2. Alamat
    // 3. Provinsi
    // 4. Kab
    // 5. Kec
    // 6. Desa
    // 7. Lat
    // 8. Long
    
    for (let i = 0; i < lines.length; i += 8) {
      if (i + 7 >= lines.length) break;
      
      const nama = lines[i];
      const alamat = lines[i+1];
      const provinsi = lines[i+2];
      const kab = lines[i+3];
      const kec = lines[i+4];
      const desa = lines[i+5];
      const latStr = lines[i+6].replace(",", ".");
      const longStr = lines[i+7].replace(",", ".");
      
      const fullLocation = `${alamat}, ${desa}, ${kec}, ${kab}, ${provinsi}`;
      
      tasks.push({
        title: "KDMP",
        description: "VISIT " + nama,
        location: fullLocation,
        reward: 10000,
        status: "pending",
        segment: "KDMP",
        latitude: parseFloat(latStr),
        longitude: parseFloat(longStr)
      });
    }

    console.log(`Found ${tasks.length} tasks to insert...`);
    
    // Opsional: Hapus data lama dengan segment KDMP agar tidak duplikat jika dijalankan ulang
    // await Task.deleteMany({ segment: "KDMP" });
    
    await Task.insertMany(tasks);
    console.log("✅ Success insert KDMP tasks!");
    
    process.exit(0);
  } catch (err) {
    console.error("❌ Error:", err);
    process.exit(1);
  }
};

seedKDMP();
